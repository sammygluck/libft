#include <unistd.h>

void ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while(s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
}

int	main(void)
{
	char str[] = "hello";

	ft_putstr_fd(str, 1);
}
